 const addUniqueIds = (data) => {
   let idCounter = 1; // Initialize the ID counter
 
   // Map through the data and add IDs
   const dataWithIds = data.map((item) => ({
     id: idCounter++, // Assign the current ID and then increment the counter
     ...item, // Spread the existing item properties
   }));
 
   return dataWithIds;
 };
 
